    public void tampil() {
        mencari = "";
        cari1 = "b.inisial_label_gizi<>'-' and b.nm_bangsal like '%" + TCari.getText() + "%'";
        cari2 = "b.inisial_label_gizi<>'-' and k.kelas like '%" + TCari.getText() + "%'";
        cari3 = "b.inisial_label_gizi<>'-' and b.inisial_label_gizi like '%" + TCari.getText() + "%'";
        cari4 = "b.inisial_label_gizi<>'-' and b.nm_gedung like '%" + TCari.getText() + "%'";

        Valid.tabelKosong(tabMode);
        try {
            mencari = cari1 + " or " + cari2 + " or " + cari3 + " or " + cari4;
            ps = koneksi.prepareStatement("SELECT DISTINCT k.kelas, b.nm_bangsal, b.inisial_label_gizi, b.nm_gedung FROM bangsal b "
                    + "INNER JOIN kamar k on k.kd_bangsal=b.kd_bangsal WHERE " + mencari + " ORDER BY k.kelas, b.nm_bangsal");

            try {
                rs = ps.executeQuery();
                while (rs.next()) {
                    tabMode.addRow(new Object[]{
                        rs.getString(1),
                        rs.getString(2),
                        rs.getString(3),
                        rs.getString(4)
                    });
                }
            } catch (Exception e) {
                System.out.println("laporan.DlgDataDietRanap.tampil() : " + e);
            } finally {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
            }
        } catch (SQLException e) {
            System.out.println("Notifikasi : " + e);
        }
    }
    
